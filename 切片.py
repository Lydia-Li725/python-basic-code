str1 = 'abcdefg'
# 得到整个字符串
print(str1)
str2 = "0123456"
print(str2[2:6:2]) # 24
print(str2[0:5]) # 01234
# 默认步长为1,从0开始选取,不写结束选到最后，都不写从开始选到最后
print(str2[:5])
print(str2[2:])
print(str2[:])
print(str2[::-1])#步长为负数表示倒序选取
print(str2[-4:-1]) #-1表示最后一个数据
print(str2[-4:-1:-1]) # 不能选取出数据。-4到-1选取方向为从左到右，-1步长表示从右到左选取
#**如果选取方向（下标方向）和步长方向冲突，则无法选取
print(str2[-1:-4:-1])